    private void jButton7ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton7ActionPerformed
        //insert details into database
        try {
            String sql = "insert into pizzaOrder(pizza,drink,toppings,pizzaPrice,drinkPrice,toppingsPrice,totalPrice,phoneNumber,name,date)values(?,?,?,?,?,?,?,?,?,?)";

            pst = conn.prepareStatement(sql);
            pst.setString(1, pizza_box.getText());
            pst.setString(2, drinks_box.getText());
            pst.setString(3, textarea_toppings.getText());
            pst.setString(4, pizza_total.getText());
            pst.setString(5, toppings_total.getText());
            pst.setString(6, drinks_total.getText());
            if (total_cost.getText().equals("")) {
                double total = totalP + totalD + totalT;
                String tot = fmt.format(total);
                pst.setString(7, tot);
            } else {
                pst.setString(7, total_cost.getText());
            }
            pst.setString(8, Global.phoneN);
            pst.setString(9, Global.customerName);
            pst.setString(10, sdf.format(cal.getTime()));

            pst.execute();
            //close the connection
//            pst.close();
  //         rs.close();
            printReceipt pReceipt = new printReceipt();
            pReceipt.setVisible(true);
            close();
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e);
        } finally {
            try {
                rs.close();
                pst.close();
            } catch (Exception e) {
            }
        }
    }//GEN-LAST:event_jButton7ActionPerformed

