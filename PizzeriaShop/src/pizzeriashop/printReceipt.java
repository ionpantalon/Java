    private void populateDataPizza() {
        try {
            String sql2 = "select * from pizzaOrder";
            pst = conn.prepareStatement(sql2);
            rs = pst.executeQuery();
            while (rs.next()) {
                if (rs.getString("phoneNumber").equals(Global.phoneN)) {
                    r_employee.setText(Global.employerName);
                    r_date.setText(mydate);
                    r_pizza.setText(rs.getString("pizza"));
                    r_pizzaPrice.setText(rs.getString("pizzaPrice"));
                    r_toppings.setText(rs.getString("toppings"));
                    r_toppingsPrice.setText(rs.getString("toppingsPrice"));
                    r_drinks.setText(rs.getString("drink"));
                    r_drinksPrice.setText(rs.getString("drinkPrice"));
                    r_totalPrice.setText(rs.getString("totalPrice"));
                }
            }
//                    rs.close(); 
//                    pst.close();
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e);
        } finally {
            try {
                rs.close();
                pst.close();
                conn.close();
            } catch (Exception e) {
            }
        }
    }

