    private void populateDataCustomer() {
        try {
            String sql = "select * from customers";
            pst = conn.prepareStatement(sql);
            rs = pst.executeQuery();
            while (rs.next()) {
                if (rs.getString("phoneNumber").equals(Global.phoneN)) {
                    r_fName.setText(rs.getString("Fname"));
                    r_lName.setText(rs.getString("Lname"));
                    r_address.setText(rs.getString("Address"));
                    r_postcode.setText(rs.getString("Postcode"));
                    r_phoneNumber.setText(rs.getString("PhoneNumber"));
                }
            }
//            rs.close();
//            pst.close();
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e);
        } finally {
            try {
                rs.close();
                pst.close();
            } catch (Exception e) {
            }
        }
    }
