    private void txt_comboBox_employeePopupMenuWillBecomeInvisible(javax.swing.event.PopupMenuEvent evt) {//GEN-FIRST:event_txt_comboBox_employeePopupMenuWillBecomeInvisible
        String tmp = (String) txt_comboBox_employee.getSelectedItem();

        try {
            pst = conn.prepareStatement("select * from employers where EmployeeName='" + tmp + "'");
            rs = pst.executeQuery();
            if (rs.next()) {
                String fName = rs.getString("EmployeeName");
                txt_name.setText(fName);
                String lName = rs.getString("LastName");
                txt_lname.setText(lName);
                String address = rs.getString("address");
                txt_address.setText(address);
                String position = rs.getString("position");
                txt_position.setText(position);
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e);
        } finally {
            try {
                rs.close();
                pst.close();
                
            } catch (Exception e) {
            }
        }
        }

