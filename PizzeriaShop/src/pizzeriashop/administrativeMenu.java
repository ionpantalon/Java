    private void fillCombo() {
        try {
            pst = conn.prepareStatement("select * from employers");
            rs = pst.executeQuery();

            while (rs.next()) {
                String name = rs.getString("EmployeeName");
                txt_comboBox_employee.addItem(name);
            }
            // pst.close();
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e);
        } finally {
            try {
                rs.close();
                pst.close();
            } catch (Exception e) {
            }
        }
    }
